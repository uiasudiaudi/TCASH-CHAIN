TEST(rust_lib, verify_merkle_proof) {
    auto leaf_b00 = from_hex("aaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaaa");
    auto leaf_b01 = from_hex("bbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbbb");
    auto leaf_b10 = from_hex("cccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccc");
    auto leaf_b11 = from_hex("dddddddddddddddddddddddddddddddddddddddddddddddddddddddddddddddd");
    auto node_b0x = from_hex("e2d80f78d79027556d6619a1400605abbdca6bb6eb24e0831e33ecd5466fa5f6");
    auto node_b1x = from_hex("5855266bbab10ac48bbac9b714edf3239b2e7d572a6a9f6d1da0a53cbfa7be0e");
    auto root = from_hex("81952b5c47f0703b5f2543a6dde2be50c5271e327c438e85c70874adf5b10e12");
    {
        xbytes_t branch_data{leaf_b01.begin(), leaf_b01.end()};
        xbytes_t verify(32);
        branch_data.insert(branch_data.end(), node_b1x.begin(), node_b1x.end());
        EXPECT_TRUE(unsafe_merkle_proof(leaf_b00.data(), branch_data.data(), branch_data.size(), 2, 0b00, verify.data()));
        EXPECT_EQ(verify, root);
    }
    {
        xbytes_t branch_data{leaf_b00.begin(), leaf_b00.end()};
        xbytes_t verify(32);
        branch_data.insert(branch_data.end(), node_b1x.begin(), node_b1x.end());
        EXPECT_TRUE(unsafe_merkle_proof(leaf_b01.data(), branch_data.data(), branch_data.size(), 2, 0b01, verify.data()));
        EXPECT_EQ(verify, root);
    }
    {
        xbytes_t branch_data{leaf_b11.begin(), leaf_b11.end()};
        xbytes_t verify(32);
        branch_data.insert(branch_data.end(), node_b0x.begin(), node_b0x.end());
        EXPECT_TRUE(unsafe_merkle_proof(leaf_b10.data(), branch_data.data(), branch_data.size(), 2, 0b10, verify.data()));
        EXPECT_EQ(verify, root);
    }
    {
        xbytes_t branch_data{leaf_b10.begin(), leaf_b10.end()};
        xbytes_t verify(32);
        branch_data.insert(branch_data.end(), node_b0x.begin(), node_b0x.end());
        EXPECT_TRUE(unsafe_merkle_proof(leaf_b11.data(), branch_data.data(), branch_data.size(), 2, 0b11, verify.data()));
        EXPECT_EQ(verify, root);
    }
    {
        xbytes_t branch_data{leaf_b10.begin(), leaf_b10.end()};
        xbytes_t verify(32);
        EXPECT_TRUE(unsafe_merkle_proof(leaf_b11.data(), branch_data.data(), branch_data.size(), 1, 0b11, verify.data()));
        EXPECT_EQ(verify, node_b1x);
    }
    {
        xbytes_t branch_data;
        xbytes_t verify(32);
        EXPECT_FALSE(unsafe_merkle_proof(leaf_b01.data(), branch_data.data(), branch_data.size(), 2, 0b01, verify.data()));
        EXPECT_NE(verify, node_b1x);
    }
    {
        xbytes_t branch_data{node_b1x.begin(), node_b1x.end()};
        xbytes_t verify(32);
        branch_data.insert(branch_data.end(), leaf_b00.begin(), leaf_b00.end());
        EXPECT_TRUE(unsafe_merkle_proof(leaf_b01.data(), branch_data.data(), branch_data.size(), 2, 0b01, verify.data()));
        EXPECT_NE(verify, root);
    }
    {
        xbytes_t branch_data{leaf_b00.begin(), leaf_b00.end()};
        xbytes_t verify(32);
        EXPECT_FALSE(unsafe_merkle_proof(leaf_b01.data(), branch_data.data(), branch_data.size(), 2, 0b01, verify.data()));
        EXPECT_NE(verify, root);
    }
    {
        xbytes_t branch_data{leaf_b00.begin(), leaf_b00.end()};
        xbytes_t verify(32);
        branch_data.insert(branch_data.end(), node_b1x.begin(), node_b1x.end());
        EXPECT_TRUE(unsafe_merkle_proof(leaf_b01.data(), branch_data.data(), branch_data.size(), 2, 0b10, verify.data()));
        EXPECT_NE(verify, root);
    }
    {
        xbytes_t branch_data{leaf_b00.begin(), leaf_b00.end()};
        xbytes_t verify(32);
        branch_data.insert(branch_data.end(), node_b1x.begin(), node_b1x.end());
        EXPECT_TRUE(unsafe_merkle_proof(leaf_b01.data(), branch_data.data(), branch_data.size(), 2, 0b10, verify.data()));
        EXPECT_NE(verify, node_b1x);
    }
    auto leaf = from_hex("D6D6D6D6D6D6D6D6D6D6D6D6D6D6D6D6D6D6D6D6D6D6D6D6D6D6D6D6D6D6D6D6");
    auto junk = from_hex("D7D7D7D7D7D7D7D7D7D7D7D7D7D7D7D7D7D7D7D7D7D7D7D7D7D7D7D7D7D7D7D7");
    {
        xbytes_t verify(32);
        EXPECT_TRUE(unsafe_merkle_proof(leaf.data(), nullptr, 0, 0, 0, verify.data()));
        EXPECT_EQ(verify, leaf);
    }
    {
        xbytes_t verify(32);
        EXPECT_TRUE(unsafe_merkle_proof(leaf.data(), nullptr, 0, 0, 7, verify.data()));
        EXPECT_NE(verify, junk);
    }
}

TEST(rust_lib, beacon_tree_root) {
    {
        xbeacon_block_header_t header;
        header.slot = 4100000;
        header.proposer_index = 176449;
        header.parent_root = evm_common::h256(from_hex("a59fd12ab2ea2ad157ad34c2411cfa85ed1b53bed2de08e5bd9d70ff31243857"));
        header.state_root = evm_common::h256(from_hex("1320073a2b354f510fca6ea85f8a580ef668eaa25af4ef5f948dbbad3e46711d"));
        header.body_root = evm_common::h256(from_hex("35047f57957bcc8be12e0ad6a40b8904df6741c25d1ed93b7a7c4ef74095bd40"));
        auto rlp = header.encode_rlp();
        xbytes_t hash_bytes(32);
        EXPECT_TRUE(unsafe_beacon_header_root(rlp.data(), rlp.size(), hash_bytes.data()));
        EXPECT_EQ(hash_bytes, from_hex("342ca1455e976f300cc96a209106bed2cbdf87243167fab61edc6e2250a0be6c"));
    }
    {
        xbeacon_block_header_t header;
        header.slot = 5000;
        header.proposer_index = 83889;
        header.parent_root = evm_common::h256(from_hex("91770717455346b00bfe97124c227847950bf265642b64e5aa60ca63d2f9013d"));
        header.state_root = evm_common::h256(from_hex("5b307edf46e936907055286c06ad852a2b94f2af8b5eba328dbdcb4010a7cedc"));
        header.body_root = evm_common::h256(from_hex("6627267cd57e0c4bed003a1a50f645efb4e1017aa3c4df00b487d2269115c48b"));
        auto rlp = header.encode_rlp();
        xbytes_t hash_bytes(32);
        EXPECT_TRUE(unsafe_beacon_header_root(rlp.data(), rlp.size(), hash_bytes.data()));
        EXPECT_EQ(hash_bytes, from_hex("c613fbf1a8e95c2aa0f76a5d226ee1dc057cce18b235803f50e7a1bde050d290"));
    }
}

